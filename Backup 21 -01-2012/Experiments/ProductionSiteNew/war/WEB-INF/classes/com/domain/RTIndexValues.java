public class RTIndexValues {

    private long id;
    private String ASPI;
    private String MPI;
    private String date;
    private String time;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getASPI() {
        return ASPI;
    }

    public void setASPI(String ASPI) {
        this.ASPI = ASPI;
    }

    public String getMPI() {
        return MPI;
    }

    public void setMPI(String MPI) {
        this.MPI = MPI;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
